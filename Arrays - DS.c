int* reverseArray(int a_count, int* a, int* result_count) {
int i,start,end,temp;
start = 0;
end = a_count-1;
while(start < end)
{
    temp = *(a + start);
    *(a + start) = *(a + end);
    *(a + end) = temp;
    start++;
    end--;
}
*result_count = a_count;
return a;
}

